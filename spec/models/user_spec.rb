require 'spec_helper'
describe User do
  it "should have some questions" do
    user1 = User.new(email: 'abc@abc.com', username: 'abc')
    user1.save(validate: false)
    question1 = Question.create(title: 'question1', content: 'Here is some content')#, user_id: 1)
    question1.user_id = user1.id
    question1.save
    expect(question1.user).to eq user1
  end
end